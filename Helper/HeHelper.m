#import "HeHelper.h"

@implementation HeHelper

+ (void)dialPhoneWithPhoneNumber:(NSString *)phoneNumber
{
    NSString *telUrl = [NSString stringWithFormat:@"tel://%@",phoneNumber];
    [[UIApplication sharedApplication] openURL:[NSURL URLWithString:telUrl]];
}

@end
